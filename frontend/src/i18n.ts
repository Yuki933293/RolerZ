export type Lang = 'zh' | 'zh-Hant' | 'en';
export type TranslationKey = keyof typeof translations.zh;

export function t(lang: Lang, key: TranslationKey): string | ((...args: never[]) => string) {
  return translations[lang][key] as string | ((...args: never[]) => string);
}

export function useT(lang: string) {
  const l = (lang === 'en' ? 'en' : lang === 'zh-Hant' ? 'zh-Hant' : 'zh') as Lang;
  return (key: TranslationKey) => translations[l][key];
}

